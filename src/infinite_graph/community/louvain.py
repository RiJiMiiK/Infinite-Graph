def louvain_reference_distance(
    features: dict[str, float],
    reference: dict[str, float],
) -> float:
    """Compute a weighted distance to a Louvain benchmark point."""
    return (
        abs(math.log((features["nodes"] + 1.0) / (reference["nodes"] + 1.0))) * 3.2
        + abs(features["edges_per_node"] - reference["edges_per_node"]) * 1.0
        + abs(features["self_loop_ratio"] - reference["self_loop_ratio"]) * 1.6
        + abs(features["reciprocal_ratio"] - reference["reciprocal_ratio"]) * 1.4
        + abs(features["resolution"] - reference["resolution"]) * 1.1
        + abs(features["randomize"] - reference["randomize"]) * 0.05
    )


def estimate_louvain_runtime_and_communities(
    graph: nx.DiGraph,
    resolution: float = 1.0,
    randomize: int = 0,
) -> dict[str, object]:
    """Estimate Louvain runtime and community count from project benchmarks."""
    features = build_graph_structure_features(graph)
    features["resolution"] = float(resolution)
    features["randomize"] = float(randomize)
    return finalize_estimate(
        features,
        LOUVAIN_ESTIMATION_REFERENCE_POINTS,
        louvain_reference_distance,
    )
